package com.raptorplan.RaptorPlan.model;

public class Attribute {
    private String name;

    public Attribute() {
    }

    public Attribute(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
